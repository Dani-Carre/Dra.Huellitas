    private void cargarDatos() {

        ConsultaDAO dao = new ConsultaDAO();
        Consulta consulta = dao.obtenerDetalleConsulta(idConsulta);

        if (consulta != null) {
            inTxtFechaConsulta.setDate(consulta.getFecha());
            txtInMotivo.setText(consulta.getMotivo());
            txtInDiagnostico.setText(consulta.getDiagnostico());
            txtInTratamiento.setText(consulta.getTratamiento());
            jTextPane1.setText(consulta.getNombreDoctor());
        }
    }

    private void guardarCambios() {
        ConsultaDAO dao = new ConsultaDAO();
        Consulta consulta = new Consulta();

        consulta.setIdConsulta(idConsulta);
        consulta.setMotivo(txtInMotivo.getText());
        consulta.setDiagnostico(txtInDiagnostico.getText());
        consulta.setTratamiento(txtInTratamiento.getText());
        //consulta.setNombreDoctor(jTextPane1.getText());

        // Leer y validar fecha
        java.util.Date fechaUtil = inTxtFechaConsulta.getDate();
        if (fechaUtil == null) {
            JOptionPane.showMessageDialog(this, "La fecha no puede estar vac√≠a.");
            return;
        }
        consulta.setFecha(new java.sql.Date(fechaUtil.getTime()));

        boolean actualizado = dao.actualizarConsulta(consulta);
        if (actualizado) {
            JOptionPane.showMessageDialog(this, "Consulta actualizada correctamente.");
            Expedientes expediente = new Expedientes();
            expediente.setVisible(true);
            setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Error al actualizar la consulta.");
        }
    }

